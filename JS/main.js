// Switch case 
let grade = "S";

switch (grade) {
    case "S":
        console.log("Super Grade");
        break;
    case "A":
    case "B":
        console.log("Excellent Grade");
        break;
    case "U":
        console.log("Just Passed");
        break;
    case "U":
        console.log("Failed Grade"); 
        break;
    default:
        console.log("Unknown Grade"); 
}